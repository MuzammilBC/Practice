package rakuten_SS;
public class Interviewee {
	String name;
	int age;
	String qualification;
	Address address;
	float percentage;
	
	public Interviewee(String name, int age, String qualification, Address address, float percentage) {
		this.name = name;
		this.age = age;
		this.qualification = qualification;
		this.address = address;
		this.percentage = percentage;
	}
	
	
}
